import { Router } from 'express';
import { invoiceController } from '../controllers/invoiceController.js';
import { authenticate, authorize } from '../middleware/auth.js';
import { validate, schemas } from '../middleware/validation.js';
import { asyncHandler } from '../middleware/errorHandler.js';
const router = Router();
router.use(authenticate);
router.get('/', validate(schemas.pagination), asyncHandler(invoiceController.list));
router.get('/overdue', asyncHandler(invoiceController.getOverdueInvoices));
router.post('/', validate(schemas.createInvoice), asyncHandler(invoiceController.create));
router.get('/:id', validate(schemas.uuidParam), asyncHandler(invoiceController.getById));
router.put('/:id', validate(schemas.uuidParam), asyncHandler(invoiceController.update));
router.post('/:id/payment', validate(schemas.recordPayment), asyncHandler(invoiceController.recordPayment));
router.post('/:id/pay/mobile-money', validate(schemas.uuidParam), asyncHandler(invoiceController.initializeMobileMoneyPayment));
router.post('/:id/pay/online', validate(schemas.uuidParam), asyncHandler(invoiceController.initializeOnlinePayment));
router.get('/verify/:reference', asyncHandler(invoiceController.verifyPayment));
router.get('/:id/pdf', validate(schemas.uuidParam), asyncHandler(invoiceController.downloadPDF));
router.post('/:id/send-reminder', validate(schemas.uuidParam), asyncHandler(invoiceController.sendPaymentReminder));
router.delete('/:id', authorize('ADMIN'), validate(schemas.uuidParam), asyncHandler(invoiceController.delete));
export default router;
