import { Router } from 'express';
import { customerController } from '../controllers/customerController.js';
import { authenticate, authorize } from '../middleware/auth.js';
import { validate, schemas } from '../middleware/validation.js';
import { asyncHandler } from '../middleware/errorHandler.js';
const router = Router();
router.use(authenticate);
router.get('/', validate(schemas.pagination), asyncHandler(customerController.list));
router.get('/search', asyncHandler(customerController.search));
router.post('/', validate(schemas.createCustomer), asyncHandler(customerController.create));
router.get('/:id', validate(schemas.uuidParam), asyncHandler(customerController.getById));
router.put('/:id', validate(schemas.updateCustomer), asyncHandler(customerController.update));
router.delete('/:id', authorize('ADMIN'), validate(schemas.uuidParam), asyncHandler(customerController.delete));
router.get('/:id/history', validate(schemas.uuidParam), asyncHandler(customerController.getHistory));
export default router;
